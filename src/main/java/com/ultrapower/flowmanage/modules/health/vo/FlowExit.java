package com.ultrapower.flowmanage.modules.health.vo;

public class FlowExit {
	private String number;
	private String time;
	private String exitName;
	private String inflow;
	private String outflow;
	private String totalflow;
	private String totalflow_perc;
	
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getExitName() {
		return exitName;
	}
	public void setExitName(String exitName) {
		this.exitName = exitName;
	}
	public String getInflow() {
		return inflow;
	}
	public void setInflow(String inflow) {
		this.inflow = inflow;
	}
	public String getOutflow() {
		return outflow;
	}
	public void setOutflow(String outflow) {
		this.outflow = outflow;
	}
	public String getTotalflow() {
		return totalflow;
	}
	public void setTotalflow(String totalflow) {
		this.totalflow = totalflow;
	}
	public String getTotalflow_perc() {
		return totalflow_perc;
	}
	public void setTotalflow_perc(String totalflow_perc) {
		this.totalflow_perc = totalflow_perc;
	}
	
	
	
	

}
